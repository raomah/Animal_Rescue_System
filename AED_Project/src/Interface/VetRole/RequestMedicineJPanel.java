    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)tblMedicine.getModel();
        model.setRowCount(0);
        for(PharmacyUnitWre unit: request.getMedicineList()) {
            Object[] row = new Object[2];
            row[0] = unit.getMedicine();
            row[1] = unit.getQuantity();
            model.addRow(row);
        }
    }
    
    public void populateType() {
        cboxType.removeAllItems();
        for (MedicineDetails.MedicineType type : MedicineDetails.MedicineType.values()) {
            cboxType.addItem(type);
        }
        cboxType.setSelectedItem(MedicineDetails.MedicineType.Anesthetics);
    }
    
    public void populateMedicine() {
        MedicineDetails.MedicineType type = (MedicineDetails.MedicineType)cboxType.getSelectedItem();
        cboxMedicine.removeAllItems();
        for (MedicineDetails m: enterprise.getMedicineDirectory().getMedicineList()) {
            if(m.getMedtype() == type) {
                cboxMedicine.addItem(m);
            }
        }
        cboxMedicine.setSelectedItem(WIDTH);
    }
    
    
