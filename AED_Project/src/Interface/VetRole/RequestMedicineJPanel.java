    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        // TODO add your handling code here:
        request.setWremessage(room.getRoomno());
        request.setWresender(userAccount);
        request.setWrestatus("Sent");
        request.setWrerequestDate(record.getDate());
        
        OrganisationMain org = null;
        for (OrganisationMain organization : enterprise.getOrganisationDirectory().getOrganisationList()){
            if (organization instanceof PharmacyOrganisation){
                org = organization;
                break;
            }
        }
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().add(request);
            userAccount.getWorkQueue().getWorkRequestList().add(request);
        }
        
        DefaultTableModel model = (DefaultTableModel)tblMedicine.getModel();
        model.setRowCount(0);
        JOptionPane.showMessageDialog(null, "Request has been sent.");
    }//GEN-LAST:event_btnSendActionPerformed

